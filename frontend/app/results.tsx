import React, { useEffect, useState } from 'react';
import {
  View,
  Text,
  StyleSheet,
  ScrollView,
  TouchableOpacity,
  SafeAreaView,
  Alert,
  Share,
} from 'react-native';
import { StatusBar } from 'expo-status-bar';
import { useRouter, useLocalSearchParams } from 'expo-router';

interface PredictionResult {
  patient_id: string;
  prediction: number;
  risk_probability: number;
  risk_level: string;
  model_used: string;
  timestamp: string;
  recommendations: string[];
}

export default function Results() {
  const router = useRouter();
  const { resultData } = useLocalSearchParams();
  const [result, setResult] = useState<PredictionResult | null>(null);

  useEffect(() => {
    if (resultData && typeof resultData === 'string') {
      try {
        const parsedResult = JSON.parse(resultData);
        setResult(parsedResult);
      } catch (error) {
        console.error('Error parsing result data:', error);
        Alert.alert('Error', 'Failed to load prediction results.');
      }
    }
  }, [resultData]);

  const getRiskColor = (riskLevel: string) => {
    switch (riskLevel) {
      case 'Low': return '#4CAF50';
      case 'Medium': return '#FF9800';
      case 'High': return '#F44336';
      default: return '#888888';
    }
  };

  const getRiskIcon = (riskLevel: string) => {
    switch (riskLevel) {
      case 'Low': return '‚úÖ';
      case 'Medium': return '‚ö†Ô∏è';
      case 'High': return 'üö®';
      default: return '‚ùì';
    }
  };

  const handleShare = async () => {
    if (!result) return;

    const shareText = `My Heart Disease Risk Assessment:
Risk Level: ${result.risk_level}
Risk Probability: ${(result.risk_probability * 100).toFixed(1)}%
Model Used: ${result.model_used}

Key Recommendations:
${result.recommendations.slice(0, 3).map((rec, index) => `${index + 1}. ${rec}`).join('\n')}

Generated by Heart Health AI`;

    try {
      await Share.share({
        message: shareText,
        title: 'Heart Disease Risk Assessment',
      });
    } catch (error) {
      console.error('Error sharing:', error);
    }
  };

  const handleNewAssessment = () => {
    router.replace('/assessment');
  };

  const handleViewHistory = () => {
    router.push('/history');
  };

  if (!result) {
    return (
      <SafeAreaView style={styles.container}>
        <StatusBar style="light" />
        <View style={styles.centerContainer}>
          <Text style={styles.errorText}>Loading results...</Text>
        </View>
      </SafeAreaView>
    );
  }

  const riskPercentage = (result.risk_probability * 100).toFixed(1);
  const riskColor = getRiskColor(result.risk_level);
  const riskIcon = getRiskIcon(result.risk_level);

  return (
    <SafeAreaView style={styles.container}>
      <StatusBar style="light" />
      
      {/* Header */}
      <View style={styles.header}>
        <TouchableOpacity style={styles.backButton} onPress={() => router.back()}>
          <Text style={styles.backButtonText}>‚Üê Back</Text>
        </TouchableOpacity>
        <Text style={styles.headerTitle}>Assessment Results</Text>
        <TouchableOpacity style={styles.shareButton} onPress={handleShare}>
          <Text style={styles.shareButtonText}>Share</Text>
        </TouchableOpacity>
      </View>

      <ScrollView style={styles.content} showsVerticalScrollIndicator={false}>
        {/* Risk Score Card */}
        <View style={styles.riskCard}>
          <Text style={styles.riskIcon}>{riskIcon}</Text>
          <Text style={styles.riskTitle}>Your Risk Level</Text>
          <Text style={[styles.riskLevel, { color: riskColor }]}>
            {result.risk_level}
          </Text>
          <Text style={styles.riskPercentage}>
            {riskPercentage}% probability
          </Text>
          <Text style={styles.riskDescription}>
            {result.risk_level === 'Low' && 'You have a relatively low risk of heart disease. Keep up the healthy habits!'}
            {result.risk_level === 'Medium' && 'You have a moderate risk of heart disease. Consider lifestyle improvements.'}
            {result.risk_level === 'High' && 'You have a higher risk of heart disease. Please consult with a healthcare professional.'}
          </Text>
        </View>

        {/* Model Info */}
        <View style={styles.infoCard}>
          <Text style={styles.infoTitle}>üìä Analysis Details</Text>
          <View style={styles.infoRow}>
            <Text style={styles.infoLabel}>Model Used:</Text>
            <Text style={styles.infoValue}>{result.model_used}</Text>
          </View>
          <View style={styles.infoRow}>
            <Text style={styles.infoLabel}>Patient ID:</Text>
            <Text style={styles.infoValue}>{result.patient_id.substring(0, 8)}...</Text>
          </View>
          <View style={styles.infoRow}>
            <Text style={styles.infoLabel}>Assessment Date:</Text>
            <Text style={styles.infoValue}>
              {new Date(result.timestamp).toLocaleDateString()}
            </Text>
          </View>
        </View>

        {/* Recommendations */}
        <View style={styles.recommendationsCard}>
          <Text style={styles.recommendationsTitle}>üí° Personalized Recommendations</Text>
          {result.recommendations.map((recommendation, index) => (
            <View key={index} style={styles.recommendationItem}>
              <Text style={styles.recommendationNumber}>{index + 1}</Text>
              <Text style={styles.recommendationText}>{recommendation}</Text>
            </View>
          ))}
        </View>

        {/* Risk Factors Info */}
        <View style={styles.infoCard}>
          <Text style={styles.infoTitle}>‚ÑπÔ∏è Understanding Your Results</Text>
          <Text style={styles.infoText}>
            This prediction is based on machine learning analysis of over 8,700 patient records. 
            The model considers 22+ risk factors including age, lifestyle, medical history, and vital signs.
          </Text>
          <Text style={styles.infoText}>
            <Text style={styles.boldText}>Important:</Text> This assessment is for informational purposes only 
            and should not replace professional medical advice. Always consult with your healthcare provider 
            for personalized medical guidance.
          </Text>
        </View>

        {/* Action Buttons */}
        <View style={styles.actionButtons}>
          <TouchableOpacity 
            style={[styles.actionButton, styles.primaryActionButton]}
            onPress={handleNewAssessment}
          >
            <Text style={styles.primaryActionText}>üîÑ New Assessment</Text>
          </TouchableOpacity>

          <TouchableOpacity 
            style={[styles.actionButton, styles.secondaryActionButton]}
            onPress={handleViewHistory}
          >
            <Text style={styles.secondaryActionText}>üìã View History</Text>
          </TouchableOpacity>
        </View>

        {/* Footer */}
        <View style={styles.footer}>
          <Text style={styles.footerText}>
            üî¨ Powered by Advanced Machine Learning
          </Text>
          <Text style={styles.footerSubtext}>
            Random Forest Model ‚Ä¢ 95%+ Accuracy
          </Text>
        </View>
      </ScrollView>
    </SafeAreaView>
  );
}

